AddEventHandler('playerSpawned', function()
	Citizen.CreateThread(function()
		for _, prop in ipairs(searchAreas) do
			x, y, z = table.unpack(GetEntityCoords(GetPlayerPed(-1), true))
			local newPropX = x
			local newPropY = y
			local newPropZ = z + 999.0
					
			repeat
				Citizen.Wait(1)
				
				newPropX = x + math.random(-750, 750)
				newPropY = y + math.random(-750, 750)
				_,newPropZ = GetGroundZFor_3dCoord(newPropX+.0,newPropY+.0,z, 1)
						
				Citizen.Wait(1)
				playerX, playerY = table.unpack(GetEntityCoords(GetPlayerPed(-1), true))
				if newPropX > playerX - 5 and newPropX < playerX + 5 or newPropY > playerY - 5 and newPropY < playerY + 5 then
					canSpawn = false
					break
				else
					canSpawn = true
				end
			until canSpawn
					
			searchableProp = searchableProps[math.random(1, #searchableProps)]
			searchProp2 = CreateObject(GetHashKey(searchableProp), prop.x, prop.y, prop.z - 1, false, false, true)
						
			blip = AddBlipForEntity(searchProp2)
			SetBlipSprite(blip, 274)
			SetBlipAsShortRange(blip, true)
			SetBlipAlpha(blip, 255)
			SetBlipScale(blip, 0.99)
						
			table.insert(searchprops, searchProp2)
			Citizen.Trace("Bag Spawned")
		end
	end)
end)

